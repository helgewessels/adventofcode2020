    def 'parse image tile'() {
        given:
        def input = '''
            Tile 2311:
            ..##.#..#.
            ##..#.....
            #...##..#.
            ####.#...#
            ##.##.###.
            ##...#.###
            .#.#.#..##
            ..#....#..
            ###...#.#.
            ..###..###
        '''

        when:
        def tiles = day20.parseTiles(input)

        then:
        tiles.first().id == 2311
        tiles.first().top == '..##.#..#.'
        tiles.first().bottom == '..###..###'
        tiles.first().left == '.#####..#.'
        tiles.first().right == '...#.##..#'
    }

